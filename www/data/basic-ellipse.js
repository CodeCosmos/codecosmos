/* ELLIPSES (and circles) */

// See also: http://processingjs.org/reference/ellipse_/

// The ellipse method takes four arguments, these are the
// x and y coordinates of the center and its width and height
// p.ellipse(x, y, width, height)

// Draw a face as a circle
p.ellipse(50, 50, 80, 80);

// Left eye (well, our left)
p.ellipse(30, 40, 15, 15);

// Right eye
p.ellipse(70, 40, 15, 15);

// Mouth
p.ellipse(50, 70, 35, 15);